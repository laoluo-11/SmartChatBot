/* =========================================================================
 * led.h —— LED 心跳灯模块的"对外说明书"
 * -------------------------------------------------------------------------
 * 头文件(.h)里只放"给别人看的声明"：配置宏 + 函数原型。
 * 具体怎么实现（怎么点亮、怎么延时）写在 led.c 里。
 *
 * 为什么要有 .h？
 *   别的文件（比如 main.c）想用这个模块时，只要 #include "led.h"，
 *   编译器就知道"有这么个 led_task 函数可用"，链接时再去 led.c 找真正实现。
 *   这就是 C 语言"声明"和"定义"分离的基本套路。
 *
 * #ifndef / #define / #endif 叫"头文件保护"：防止同一个 .h 被多次包含时报重复定义。
 * ========================================================================= */
#ifndef LED_H
#define LED_H

/* ===== 板载 RGB LED 配置（寻址灯 WS2812 / SK68xx，靠 RMT 时序协议控制，不是普通 GPIO）=====
 * ESP32-S3-DevKitC 系列板载 RGB 灯默认接 GPIO48。
 * 若你的板子接的是别的脚（少数板子用 GPIO38），只改这里即可。 */
#define RGB_LED_GPIO     48      // 板载 RGB 灯数据脚
#define RGB_LED_R        0       // 心跳点亮时的颜色 R 分量 (0-255)
#define RGB_LED_G        80      // 心跳点亮时的颜色 G 分量 (0-255) —— 偏青绿，醒目又不刺眼
#define RGB_LED_B        255     // 心跳点亮时的颜色 B 分量 (0-255)

/* 心跳灯任务：让板载 RGB 灯每 1 秒亮灭一次。
 * 参数 pvParameters 是 FreeRTOS 创建任务时可能传进来的"入参"，本任务用不到。 */
void led_task(void *pvParameters);

#endif /* LED_H */
